class LLMUnavailable(Exception):
    """Прокси/LLM недоступен — нужно использовать rule-based fallback."""


class LLMClient:
    """Тонкая обёртка над openai SDK для общения через openclaw-proxy.

    Реализует tool-calling loop: запрос → tool_calls → выполнение инструментов →
    повторный запрос с результатами → финальный ответ.
    """

    def __init__(
        self,
        base_url: str | None = None,
        api_key: str | None = None,
        model: str = "claude-sonnet-4-6",
        max_iterations: int = 8,
        timeout: float = 180.0,
        system_prompt: str | None = None,
    ):
        self.base_url = base_url or os.getenv("OPENAI_BASE_URL")
        # openclaw принимает любой ключ; OPENAI_API_KEY=dummy для прокси
        self.api_key = api_key or os.getenv("OPENAI_API_KEY", "dummy-key-for-proxy")
        self.model = os.getenv("LLM_MODEL", model)
        self.max_iterations = int(os.getenv("LLM_MAX_ITERATIONS", max_iterations))
        self.timeout = float(os.getenv("LLM_TIMEOUT", timeout))

        if not self.base_url:
            raise LLMUnavailable("OPENAI_BASE_URL не задана — LLM-режим выключен")

        # max_retries=0 — на тяжёлых tool-loop запросах ретраи бесполезны (та же
        # очередь tool_calls будет крутиться заново), а ожидание удлиняется кратно.
        self._client = AsyncOpenAI(
            base_url=self.base_url,
            api_key=self.api_key,
            timeout=self.timeout,
            max_retries=0,
        )

    async def health_check(self) -> bool:
        """Проверка доступности прокси. Делаем минимальный запрос."""
        try:
            resp = await self._client.chat.completions.create(
                model=self.model,
                messages=[{"role": "user", "content": "ping"}],
                max_tokens=5,
            )
            return resp.choices[0].message is not None
        except (APIError, APIConnectionError, asyncio.TimeoutError) as e:
            log.warning(f"LLM health-check failed: {e}")
            return False

    async def chat(self, user_message: str, history: list[dict] | None = None) -> str:
        """Полный turn: запрос → tool-loop → финальный ответ."""
        messages: list[dict[str, Any]] = [{"role": "system", "content": SYSTEM_PROMPT}]
        if history:
            messages.extend(history)
        messages.append({"role": "user", "content": user_message})

        for iteration in range(self.max_iterations):
            try:
                response = await self._client.chat.completions.create(
                    model=self.model,
                    messages=messages,
                    tools=bot_tools.TOOLS_SPEC,
                    tool_choice="auto",
                    max_tokens=2000,
                )
            except (APIError, APIConnectionError, asyncio.TimeoutError) as e:
                log.error(f"LLM error: {e}")
                raise LLMUnavailable(str(e)) from e

            msg = response.choices[0].message

            if not msg.tool_calls:
                # Финальный ответ от LLM
                raw = (msg.content or "").strip() or "Извините, не удалось сформулировать ответ."
                clean, was_filtered = _sanitize_reply(raw)
                if was_filtered:
                    log.warning(f"LLM выдал недопустимый текст, заменено. Был: {raw[:200]!r}")
                return clean

            # Есть tool_calls — выполняем и возвращаем результаты в контекст
            messages.append({
                "role": "assistant",
                "content": msg.content,
                "tool_calls": [tc.model_dump() for tc in msg.tool_calls],
            })
            for tc in msg.tool_calls:
                name = tc.function.name
                args = tc.function.arguments
                log.info(f"LLM calls tool: {name}({args[:150]}...)")
                result = await bot_tools.call_tool(name, args)
                messages.append({
                    "role": "tool",
                    "tool_call_id": tc.id,
                    "content": result,
                })

        log.warning("Превышен лимит tool-итераций — возвращаю частичный ответ")
        return "Извините, я слишком долго думал. Попробуйте сформулировать вопрос иначе."


def make_client() -> LLMClient | None:
    """Создаёт клиента, если конфигурация позволяет. Иначе None."""
    try:
        client = LLMClient()
        return client
    except LLMUnavailable as e:
        log.info(f"LLM не настроен: {e}. Бот будет работать в rule-based режиме.")
        return None
