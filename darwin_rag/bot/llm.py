# от случайных «протечек» цундере/ролевых тонов.
_FORBIDDEN_PATTERNS = [
    re.compile(p, re.I) for p in (
        r"\bб-?бак",     # «Б-бака», «бака», «б-бакалай»
        r"\bбака\b",
        r"\bтупиц",
        r"\bидиот",
        r"\bдебил",
        r"\bпридур",
        r"\bдурак\b",
        r"\bуёб",
        r"\bмудак",
        r"\bонии[- ]?сан\b",
        r"\b-[чк]ан\b",
        r"асуна",
        r"NSFW",
        r"анал[иь]",  # анальный (NSFW)
        r"эякул",
        r"оргаз",
        r"\bsex\b",
        r"ahegao",
        r"hentai",
    )
]

_STANDARD_REFUSAL = (
    "Это не входит в мою задачу. Я консультант питомника «Сажень», "
    "могу помочь по сортам яблони для Томска. Чем подсказать?"
)


def _sanitize_reply(text: str) -> tuple[str, bool]:
    """Возвращает (text, was_filtered). Если найдено запрещённое — отдаём стандартный отказ."""
    for pat in _FORBIDDEN_PATTERNS:
        if pat.search(text):
            log.warning(f"Output filter: совпал паттерн {pat.pattern!r}, заменяю ответ на стандартный")
            return _STANDARD_REFUSAL, True
    return text, False


